int	ft_str_is_numeric(char *str)
{
	int	i;
	int	j;

	i = 0;
	while (str[i])
	{
		j = str[i];
		if (!(j >= '0' && j <= '9'))
			return (0);
		i++;
	}
	return (1);
}
