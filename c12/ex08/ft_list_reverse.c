#include "ft_list.h"

t_list	*ft_list_last(t_list *begin_list)
{
	while (begin_list->next)
		begin_list = begin_list->next;
	return (begin_list);
}

void	ft_list_reverse(t_list **begin_list)
{
	t_list	*before;
	t_list	*after;

	before = *begin_list;
	after = ft_list_last(before);
}
