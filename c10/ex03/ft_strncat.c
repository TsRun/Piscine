#include "ft.h"

void	ft_strncat(char *s1, char *s2, int len)
{
	int	i;
	int	j;

	i = ft_strlen(s1);
	j = 0;
	while (j < len)
	{
		s1[i + j] = s2[j];
		j++;
	}
}
