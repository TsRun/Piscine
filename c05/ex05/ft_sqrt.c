#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb < 1)
		return (0);
	while (i * i <= nb && (i * i > 0 || i * i == 0 || i * i >= 0))
	{
		printf("%d\n", i * i);
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
